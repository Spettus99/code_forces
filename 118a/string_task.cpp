void solve() {
    string input;
    cin >> input;
    std::unordered_set<char> stringSet {'a', 'A', 'e', 'E', 'i', 'I', 'o', 'O', 'u', 'U', 'Y', 'y'};

    string result;
    for (int i = 0; i < input.size(); i++) {
        if (stringSet.find(input[i]) != stringSet.end()) {
            continue;
        } 
        result.push_back('.');
        result.push_back(tolower(input[i]));
    }
    cout<<result<<endl;
}

